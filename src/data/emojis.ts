export const allEmojis = emojiCategories.flatMap((c) => c.emojis)

export function randomEmoji(from?: string[]): string {
  const pool = from ?? allEmojis
  return pool[Math.floor(Math.random() * pool.length)]!
}

export function searchEmojis(query: string): string[] {
  if (!query) return []
  const q = query.toLowerCase()
  const matches: string[] = []
  for (const cat of emojiCategories) {
    if (cat.id.includes(q) || cat.label.toLowerCase().includes(q)) {
      matches.push(...cat.emojis)
    }
  }
  return matches
}
